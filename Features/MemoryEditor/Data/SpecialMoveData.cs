        public static Dictionary<string, SpecialMove> MovesByHexId { get; } = CreateMovesByHexId();

        private static Dictionary<string, SpecialMove> CreateMovesByHexId()
        {
            var dict = new Dictionary<string, SpecialMove>();
            foreach (var move in AllMoves)
            {
                if (!dict.ContainsKey(move.HexId))
                {
                    dict[move.HexId] = move;
                }
            }
            return dict;
        }

        public static string GetMoveName(string hexId)
        {
            if (string.IsNullOrEmpty(hexId)) return "Unknown";
            if (MovesByHexId.TryGetValue(hexId.ToUpper(), out var move))
            {
                return move.Name;
            }
            return "Unknown";
        }
